package umsg;

public class Umsg {
String uname;
String utime;
String ucontent;
	public void setUname(String uname) {
		this.uname = uname;
	}
	public void setUtime(String utime) {
		this.utime = utime;
	}
	public void setUcontent(String ucontent) {
		this.ucontent = ucontent;
	}
	public String getUtime() {
		return utime;
	}
	public String getUname() {
		return uname;
	}
	public String getUcontent() {
		return ucontent;
	}	
}
